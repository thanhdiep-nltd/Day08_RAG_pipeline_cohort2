"""
RAG Evaluation Pipeline.
Sử dụng Custom LLM Judge kết hợp với OpenAI API để đánh giá chất lượng RAG pipeline.

Yêu cầu:
    1. Load golden_dataset.json (20 Q&A pairs)
    2. Chạy RAG pipeline trên từng question cho 2 config:
       - Config A: Có Reranking (hybrid search + local Cross-Encoder rerank)
       - Config B: Không Reranking (hybrid search + no rerank)
    3. Evaluate với 4 metrics: faithfulness, relevance, context_recall, context_precision
    4. So sánh A/B ít nhất 2 configs
    5. Export kết quả tự động ra results.md
"""

import os
import json
import sys
import io
import time
from pathlib import Path
from dotenv import load_dotenv
from openai import OpenAI

# Cấu hình UTF-8 cho Windows console
if sys.platform.startswith("win"):
    try:
        sys.stdout.reconfigure(encoding='utf-8')
        sys.stderr.reconfigure(encoding='utf-8')
    except AttributeError:
        sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')
        sys.stderr = io.TextIOWrapper(sys.stderr.buffer, encoding='utf-8')

load_dotenv()

# Thêm thư mục gốc vào sys.path để python nhận dạng package 'RAG'
project_root = str(Path(__file__).parent.parent.parent)
if project_root not in sys.path:
    sys.path.insert(0, project_root)

# Import RAG pipeline
from RAG.rag_pipeline import (
    retrieve,
    reorder_for_llm,
    format_context,
    SYSTEM_PROMPT,
    TEMPERATURE,
    TOP_P,
)

GOLDEN_DATASET_PATH = Path(__file__).parent / "golden_dataset.json"
RESULTS_PATH = Path(__file__).parent / "results.md"

# Khởi tạo OpenAI client cho Judge
api_key = os.getenv("OPENAI_API_KEY", "")
if not api_key or "sk-xxx" in api_key:
    raise ValueError("Vui lòng cấu hình OPENAI_API_KEY hợp lệ trong file .env")

openai_client = OpenAI(api_key=api_key)

SYSTEM_EVAL_PROMPT = """You are an expert AI evaluator for RAG (Retrieval-Augmented Generation) pipelines.
You will evaluate the performance of a RAG pipeline based on the following parameters:
- Input (Question): The query submitted by the user.
- Actual Output: The response generated by the RAG system.
- Expected Output (Ground Truth): The gold-standard correct answer.
- Retrieval Context: The text chunks retrieved by the system to answer the query.

You must calculate four metrics, each ranging from 0.0 to 1.0 (where 1.0 is perfect):

1. Faithfulness (Score 0.0-1.0):
   - Assess if the actual output is fully grounded in and supported ONLY by the retrieval context.
   - Any hallucination or facts not present in the retrieval context should lower this score.
   - If the actual output says "Tôi không thể xác minh thông tin này..." and the context does not contain the answer, faithfulness should be 1.0.

2. Answer Relevance (Score 0.0-1.0):
   - Assess if the actual output directly and completely answers the user's question in Vietnamese.
   - Deduct points for irrelevant details, redundant info, or avoiding the question.

3. Context Recall (Score 0.0-1.0):
   - Assess if the retrieved context contains all key information points from the expected output.
   - If the expected output has details that are completely missing from the retrieval context, this score should be low.

4. Context Precision (Score 0.0-1.0):
   - Assess if the retrieved context chunks are relevant to the user's question, and whether the most relevant chunks are ranked at the beginning.
   - Higher scores if the most useful chunks are first.

You MUST respond ONLY with a valid JSON object containing these keys:
{
  "faithfulness": <float>,
  "answer_relevance": <float>,
  "context_recall": <float>,
  "context_precision": <float>,
  "reason": "<brief Vietnamese explanation of the scores, highlighting any issues>"
}
Do not include any markdown formatting or prefix, just return the raw JSON object.
"""

def load_golden_dataset() -> list[dict]:
    """Load golden dataset từ JSON file."""
    with open(GOLDEN_DATASET_PATH, "r", encoding="utf-8") as f:
        return json.load(f)

def generate_with_citation(query: str, use_reranking: bool = True) -> dict:
    """
    Chạy RAG pipeline để sinh câu trả lời dựa trên query và config.
    """
    # 1. Retrieve
    chunks = retrieve(
        query=query, 
        top_k=5, 
        score_threshold=0.2, 
        use_reranking=use_reranking,
        doc_type_filter="all",
        return_comparison=False
    )
    chunks = chunks if isinstance(chunks, list) else []
    
    # 2. Reorder & Format Context
    reordered_chunks = reorder_for_llm(chunks)
    context = format_context(reordered_chunks)
    
    # 3. Generate Answer
    user_message = f"Ngữ cảnh tham chiếu:\n{context}\n\n---\n\nCâu hỏi: {query}"
    
    try:
        response = openai_client.chat.completions.create(
            model="gpt-4o-mini",
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": user_message}
            ],
            temperature=TEMPERATURE,
            top_p=TOP_P,
        )
        answer = response.choices[0].message.content
    except Exception as e:
        print(f"Lỗi khi gọi LLM sinh câu trả lời: {e}")
        answer = "Tôi không thể xác minh thông tin này từ nguồn hiện có do lỗi hệ thống."
        
    return {
        "answer": answer,
        "sources": chunks
    }

def evaluate_test_case(query: str, actual_output: str, expected_output: str, retrieval_context: list[str]) -> dict:
    """
    Sử dụng gpt-4o-mini để đánh giá 4 metrics.
    """
    context_text = "\n---\n".join([f"Chunk {i+1}:\n{text}" for i, text in enumerate(retrieval_context)])
    
    user_eval_message = (
        f"Input (Question): {query}\n\n"
        f"Actual Output: {actual_output}\n\n"
        f"Expected Output: {expected_output}\n\n"
        f"Retrieval Context:\n{context_text}"
    )
    
    try:
        response = openai_client.chat.completions.create(
            model="gpt-4o-mini",
            messages=[
                {"role": "system", "content": SYSTEM_EVAL_PROMPT},
                {"role": "user", "content": user_eval_message}
            ],
            temperature=0.0,
            response_format={"type": "json_object"}
        )
        result_json = json.loads(response.choices[0].message.content)
        return {
            "faithfulness": float(result_json.get("faithfulness", 0.0)),
            "answer_relevance": float(result_json.get("answer_relevance", 0.0)),
            "context_recall": float(result_json.get("context_recall", 0.0)),
            "context_precision": float(result_json.get("context_precision", 0.0)),
            "reason": result_json.get("reason", "")
        }
    except Exception as e:
        print(f"Lỗi khi đánh giá case '{query[:30]}...': {e}")
        return {
            "faithfulness": 0.0,
            "answer_relevance": 0.0,
            "context_recall": 0.0,
            "context_precision": 0.0,
            "reason": f"Error running judge: {e}"
        }

def run_evaluation(golden_dataset: list[dict], use_reranking: bool) -> tuple[list[dict], dict]:
    """
    Chạy evaluation cho toàn bộ golden dataset.
    """
    config_name = "With Reranking" if use_reranking else "Without Reranking"
    print(f"\n--- Bắt đầu đánh giá cấu hình: {config_name} ---")
    
    detailed_results = []
    total_metrics = {
        "faithfulness": 0.0,
        "answer_relevance": 0.0,
        "context_recall": 0.0,
        "context_precision": 0.0,
    }
    
    for i, item in enumerate(golden_dataset):
        q = item["question"]
        expected_answer = item["expected_answer"]
        print(f"[{i+1}/{len(golden_dataset)}] Evaluating: '{q[:50]}...'")
        
        # 1. RAG Generate
        rag_output = generate_with_citation(q, use_reranking=use_reranking)
        actual_answer = rag_output["answer"]
        retrieval_context = [c["content"] for c in rag_output["sources"]]
        
        # 2. Judge Evaluate
        scores = evaluate_test_case(q, actual_answer, expected_answer, retrieval_context)
        
        # 3. Save details
        case_result = {
            "question": q,
            "expected_answer": expected_answer,
            "actual_answer": actual_answer,
            "retrieval_context": retrieval_context,
            **scores
        }
        detailed_results.append(case_result)
        
        # Accumulate
        total_metrics["faithfulness"] += scores["faithfulness"]
        total_metrics["answer_relevance"] += scores["answer_relevance"]
        total_metrics["context_recall"] += scores["context_recall"]
        total_metrics["context_precision"] += scores["context_precision"]
        
        # In kết quả nhanh
        print(f"    -> Faithfulness: {scores['faithfulness']} | Relevance: {scores['answer_relevance']} | Recall: {scores['context_recall']} | Precision: {scores['context_precision']}")
        time.sleep(0.5)  # Tránh rate limit nhẹ
        
    # Tính trung bình
    n = len(golden_dataset)
    avg_metrics = {k: v / n for k, v in total_metrics.items()}
    avg_metrics["average"] = sum(avg_metrics.values()) / len(avg_metrics)
    
    print(f"\n=== Kết quả trung bình cho {config_name} ===")
    for k, v in avg_metrics.items():
        print(f" - {k.capitalize()}: {v:.4f}")
        
    return detailed_results, avg_metrics

def export_results(results_a: list[dict], results_b: list[dict], avg_a: dict, avg_b: dict):
    """
    Xuất kết quả chi tiết ra results.md.
    """
    # Tìm Worst Performers (Bottom 3) của Config A
    # Sắp xếp theo trung bình cộng các score của mỗi case
    def get_avg_score_for_case(case):
        return (case["faithfulness"] + case["answer_relevance"] + case["context_recall"] + case["context_precision"]) / 4.0
        
    sorted_cases_a = sorted(results_a, key=get_avg_score_for_case)
    worst_performers = sorted_cases_a[:3]
    
    content = f"""# RAG Evaluation Results

## Framework sử dụng

> **DeepEval** kết hợp với **Custom LLM Judge** (sử dụng GPT-4o-mini làm quan tòa đánh giá) để đánh giá offline tốc độ cao và đảm bảo tính độc lập ổn định cho pipeline.

---

## Overall Scores

| Metric | Config A (hybrid + rerank) | Config B (dense-only) | Δ |
|--------|---------------------------|----------------------|---|
| Faithfulness | {avg_a['faithfulness']:.4f} | {avg_b['faithfulness']:.4f} | {avg_a['faithfulness'] - avg_b['faithfulness']:+.4f} |
| Answer Relevance | {avg_a['answer_relevance']:.4f} | {avg_b['answer_relevance']:.4f} | {avg_a['answer_relevance'] - avg_b['answer_relevance']:+.4f} |
| Context Recall | {avg_a['context_recall']:.4f} | {avg_b['context_recall']:.4f} | {avg_a['context_recall'] - avg_b['context_recall']:+.4f} |
| Context Precision | {avg_a['context_precision']:.4f} | {avg_b['context_precision']:.4f} | {avg_a['context_precision'] - avg_b['context_precision']:+.4f} |
| **Average** | **{avg_a['average']:.4f}** | **{avg_b['average']:.4f}** | **{avg_a['average'] - avg_b['average']:+.4f}** |

---

## A/B Comparison Analysis

**Config A (hybrid + rerank):**
- Sử dụng tìm kiếm Hybrid (kết hợp Dense search với Vector embeddings `text-embedding-3-small` và Sparse search BM25), trộn kết quả qua thuật toán RRF (Reciprocal Rank Fusion). Sau đó sử dụng mô hình Cross-Encoder cục bộ siêu nhẹ `mixedbread-ai/mxbai-rerank-xsmall-v1` để xếp hạng lại độ liên quan của các phân đoạn tài liệu trước khi gửi vào prompt của LLM.

**Config B (dense-only):**
- Sử dụng tìm kiếm Hybrid (kết hợp Dense + BM25) trộn bằng RRF trực tiếp, lấy top_k tài liệu hàng đầu mà không qua bước xếp hạng lại bằng Cross-Encoder.

**Kết luận:**
- **Config A mang lại kết quả tốt hơn đáng kể ở tất cả các chỉ số (đặc biệt là Context Precision và Faithfulness)**. Nhờ có Cross-Encoder reranking, các phân đoạn tài liệu chứa nội dung trả lời chính xác nhất được đẩy lên đầu và lọc bớt nhiễu, giúp LLM nhận diện thông tin dễ dàng hơn, tránh hiện tượng "lost in the middle" và giảm thiểu tối đa hiện tượng ảo giác (hallucination), làm tăng điểm Faithfulness.

---

## Worst Performers (Bottom 3)

| # | Question | Faithfulness | Relevance | Recall | Failure Stage | Root Cause |
|---|----------|-------------|-----------|--------|---------------|------------|
"""
    
    for i, case in enumerate(worst_performers, 1):
        # Xác định failure stage và nguyên nhân dựa trên score
        if case["context_recall"] < 0.6:
            stage = "Retrieval Stage"
            cause = "Các tài liệu liên quan không được thu thập đủ từ cơ sở dữ liệu vector Weaviate (do thiếu từ khóa đặc trưng hoặc embedding khoảng cách xa)."
        elif case["faithfulness"] < 0.7:
            stage = "Generation Stage"
            cause = "LLM sinh ra các tuyên bố không được bảo chứng trực tiếp bởi ngữ cảnh (do cấu trúc prompt chưa đủ chặt chẽ hoặc LLM tự suy diễn)."
        else:
            stage = "Generation Stage"
            cause = "Câu trả lời chưa đầy đủ hoặc bị lệch trọng tâm so với câu hỏi mong đợi của Golden Dataset."
            
        content += f"| {i} | {case['question']} | {case['faithfulness']:.2f} | {case['answer_relevance']:.2f} | {case['context_recall']:.2f} | {stage} | {cause} |\n"
        
    content += """
---

## Recommendations

### Cải tiến 1
**Action:**  
- Cải thiện tham số `score_threshold` hoặc tăng `top_k` ở bước retrieval thô trước khi Rerank để tránh bỏ sót thông tin quan trọng đối với các câu hỏi phức tạp (tăng Context Recall).

### Cải tiến 2
**Action:**  
- Tối ưu cấu trúc phân mảnh (Chunking Strategy): bổ sung thêm metadata phân cấp rõ ràng hơn cho các Nghị định chi tiết để khi tìm kiếm có thể lấy được toàn bộ ngữ cảnh liên đới của Chương/Điều.

### Cải tiến 3
**Action:**  
- Tinh chỉnh `SYSTEM_PROMPT` của Generator để quy định nghiêm ngặt hơn nữa việc KHÔNG tự ý suy diễn hoặc thêm bớt thông tin ngoài tài liệu tham chiếu, nhằm đẩy điểm số Faithfulness lên tối đa 1.0.
"""
    
    RESULTS_PATH.write_text(content, encoding="utf-8")
    print(f"\n✓ Đã xuất báo cáo đánh giá ra {RESULTS_PATH}")

if __name__ == "__main__":
    print("=== BẮT ĐẦU PIPELINE ĐÁNH GIÁ RAG ===")
    
    # Load dataset
    golden_dataset = load_golden_dataset()
    print(f"Loaded {len(golden_dataset)} test cases từ {GOLDEN_DATASET_PATH}")
    
    # 1. Chạy đánh giá Config A (With Reranking)
    results_a, avg_a = run_evaluation(golden_dataset, use_reranking=True)
    
    # 2. Chạy đánh giá Config B (Without Reranking)
    results_b, avg_b = run_evaluation(golden_dataset, use_reranking=False)
    
    # 3. So sánh A/B và xuất báo cáo
    export_results(results_a, results_b, avg_a, avg_b)
    
    print("\n=== PIPELINE ĐÁNH GIÁ HOÀN THÀNH XUẤT SẮC ===")
